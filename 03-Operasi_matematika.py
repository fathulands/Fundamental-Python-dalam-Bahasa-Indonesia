a, b = 10, 3

print(a + b)        # Penjumlahan   -> 13
print(a - b)        # Pengurangan   -> 7
print(a * b)        # Perkalian     -> 30
print(a / b)        # Pembagian     -> 3.333...
print(a // b)       # Bagi bulat    -> 3
print(a % b)        # Sisa bagi     -> 1
print(a ** b)       # Pangkat       -> 1000
